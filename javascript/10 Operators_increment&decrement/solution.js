//Operators

let a = 10;
let b = 4;
console.log("a + b = ", a+b)
console.log("a - b = ", a-b)
console.log("a * b = ", a*b)
console.log("a / b = ", a/b)
console.log("a % b = ", a%b)
console.log("a ** b = ", a**b)

console.log("++a = ", ++a)//first increment then print 11
console.log("a++ = ", a++)//first print then increment 12 (output will show 11,but for the next expression value assigned is 12 if it exists)
console.log("--a = ", --a) //11
console.log("a-- = ", a--) //11
console.log("a = ", a) //10
console.log("a-- =", a--) // 10

// expected output
//11
// 11
// 11
// 11
// 10
