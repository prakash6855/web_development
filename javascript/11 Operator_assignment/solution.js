// Arithmetic operator
let a = 10;
console.log(a); //10
a += 5; // 5 will be added to the value of a
console.log(a); //15
a -= 5;
console.log(a); //10
a *= 5;
console.log(a); //50
a /= 5;
console.log(a); //10


